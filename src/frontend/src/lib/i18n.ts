export type TranslationKey = keyof typeof dashboardTranslations['en-US'];

type DotPrefix<TPrefix extends string, TKey extends string> = `${TPrefix}.${TKey}`;

type DotNestedKeys<T> = {
  [K in keyof T & string]: T[K] extends string
    ? K
    : T[K] extends Record<string, unknown>
      ? DotPrefix<K, DotNestedKeys<T[K]>>
      : never;
}[keyof T & string];

export type DashboardTranslationKey = DotNestedKeys<typeof dashboardTranslations['en-US']>;

export function translate(locale: AppLocale, key: DashboardTranslationKey, params?: Record<string, string | number>): string {
  const segments = key.split('.');
  let value: unknown = dashboardTranslations[locale];

  for (const segment of segments) {
    value = typeof value === 'object' && value !== null
      ? (value as Record<string, unknown>)[segment]
      : undefined;
  }

  const fallback = segments.reduce<unknown>(
    (current, segment) => typeof current === 'object' && current !== null
      ? (current as Record<string, unknown>)[segment]
      : undefined,
    dashboardTranslations[DEFAULT_LOCALE],
  );

  const template = typeof value === 'string'
    ? value
    : typeof fallback === 'string'
      ? fallback
      : key;

  if (!params) return template;

  return Object.entries(params).reduce(
    (result, [name, paramValue]) => result.replaceAll(`{${name}}`, String(paramValue)),
    template,
  );
}
